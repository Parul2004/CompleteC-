#include <iostream>
using namespace std;

int main()
{
    int n1, n2;
    /*

    "cout" keyword is used to print
    "cin" keyword is used to take input at run time
     " << " -> this is insertion operator
     " >> " -> this is extraction operator
     */
    cout << "Enter your first number1\n";
    cin >> n1;
    cout << "Enter your second number2\n";
    cin >> n2;
    cout << "Sum is : " << n1 + n2 << endl;
    return 0;
}